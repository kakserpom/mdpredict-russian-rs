/// Check if a word ends with any of the given suffixes
#[must_use]
pub fn ends_with_any(word: &str, suffixes: &[&str]) -> bool {
    let word_lower = word.to_lowercase();
    suffixes.iter().any(|suffix| word_lower.ends_with(suffix))
}
